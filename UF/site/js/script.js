    function quantitySwitch(){
      $(document).on('click','.amount .minus,.amount .plus',function(e){
        var input=$(this).parent().find('input'),
          val=parseInt(input.val());
        if($(this).is('.plus')){
          input.val((++val))
        }else{
          if(val-1<=1)
            input.val("1")
          else
            input.val((--val)+"")
        }
        e.preventDefault();
      });
    };

